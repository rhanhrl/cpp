#include <iostream>
using namespace std;

int main(){
    int a = 10;
    a++; //post increment
    cout << a << endl;

    int b = 20;
    ++b; // pre increment
    cout << b << endl;

    int c = 30;
    c++; // post increment
    cout << c << endl;

    int d = 40;
    ++d; // pre increment
    cout << d << endl;
}